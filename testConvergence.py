def randomMatrix():
    a = []
    i = 0
    while i < N:
        first = random.randint(0, 9)
        second = random.randint(0, 9)
        while second == first:
            second = random.randint(0, 9)
        third = random.randint(0, 9)
        while third == first or third == second:
            third = random.randint(0, 9)

        raw = []
        j = 0
        while j < N:
            if j == first or j == second or j == third:
                raw.append(1/3)
            else:
                raw.append(0)
            j += 1
        a.append(raw)
        i += 1
    return a

